public class Swapp1{
	public static void main (String args[]){
		System.out.println("Before swapping");
		int a=10;
		int b=20;
		int temp ;
		System.out.println("value of a is "+a);
		System.out.println("value of b is "+b);
		 temp= a;
		 a=b;
		 b=temp;
		 
		System.out.println("After swapping");
		System.out.println("value of a is "+a);
		System.out.println("value of b is "+b);
	}
}